import pandas as pd

fish=pd.read_csv('https://github.com/YBI-Foundation/Dataset/raw/main/Fish.csv')

fish.head()

fish.info()

fish.describe()

fish.columns

y=fish['Weight']
x=fish[['Category', 'Height', 'Width', 'Length1',
       'Length2', 'Length3']]

x

from sklearn.model_selection import train_test_split
x_train,x_test,y_train,y_test=train_test_split(x,y,test_size=0.3,random_state=2529)

from sklearn.linear_model import LinearRegression
model=LinearRegression()

model.fit(x_train,y_train)

model.intercept_

model.coef_

y_prediction=model.predict(x_test)

y_prediction

from sklearn.metrics import mean_absolute_error,r2_score

mean_absolute_error(y_test,y_prediction)

r2_score(y_test,y_prediction)

new_data=pd.DataFrame([[1,	11.5200,	4.0200,	23.2,	25.4,	30.0]],['Weight'])

new_prediction=model.predict(new_data)

print("the weigth of the fish is:",new_prediction)